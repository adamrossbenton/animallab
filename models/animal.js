////////////////////////////////////////////////
// Dependencies
////////////////////////////////////////////////
const { Schema } = require('mongoose')
const mongoose = require('./connection')

////////////////////////////////////////////////
// Models
////////////////////////////////////////////////
const {Schema, model} = mongoose

const AnimalSchema = new Schema({
    species: String,
    extinct: Boolean,
    location: String,
    lifeExpectancy: Number
})

const Animal = model("Animal", AnimalSchema)
////////////////////////////////////////////////
// Export
////////////////////////////////////////////////
module.exports = Animal